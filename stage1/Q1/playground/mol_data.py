def get_mol_geo(name:str, fmt:str='mq'):
  assert name in GEOMETRY
  assert fmt in ['chq', 'mq']

  geo = GEOMETRY[name]
  if fmt == 'mq': return geo
  
  for i, v in enumerate(geo):
    geo[i] = ' '.join([str(e) for e in [v[0]] + v[1]])
  return geo
